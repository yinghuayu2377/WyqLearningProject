package com.example.sd.learningproject.listview.customlistview;

public class Fruit {
    private String name;
    private String desc;

    public Fruit(String name, String desc) {
        this.name = name;
        this.desc = desc;
    }

    public String getName() {
        return this.name;
    }

    public String getDesc() {
        return this.desc;
    }
}
